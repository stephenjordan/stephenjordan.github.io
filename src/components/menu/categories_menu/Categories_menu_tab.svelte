    import {to_display, algs_of_cat_menu, display_algorithms} from "../../../storage/globalStore";

    export let data;

    function act() {
        if (data.name === "All algorithms") {
            $to_display = "all_algs";
        } else {
            $algs_of_cat_menu = data.name;
            $display_algorithms = true;
        }
    }

